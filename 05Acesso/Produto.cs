   O que são modificadores de Acesso?
   um modificador de acesso é como as classes vão ver atributos e metodos
   public ->Metodos e atributos visiveis  em qualquer classe
   private ->Atributos e metodos visiveis apenas em classes onde foram criadas 
   protected ->Atributos  e metodos  visiveis em classes  onde são  criados  ou herdados 
   */  
   //Ex:
   public string nome;
   private double valor;
}